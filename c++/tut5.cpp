#include<iostream>
using namespace std;

int main(){

    int num1,num2;
    cout<<"Enter the num1:\n";// "<<" is called insertion operator
    cin>>num1;// ">>" is called extraction operator
    cout<<"Enter the num2;\n";
    cin>>num2;
    cout<<"The sum of num1 and num2 is:\n"<<num1+num2<<endl;
    return 0;
}